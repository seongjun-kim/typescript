{
  /**
   * Print Loading State
   */
  type LoadingState = {
    state: "loading";
  };

  type SuccessState = {
    state: "success";
    response: {
      body: string;
    };
  };

  type FailState = {
    state: "fail";
    reason: string;
  };

  type ResourceLoadState = LoadingState | SuccessState | FailState;

  function printLoginState(data: ResourceLoadState) {
    const { state } = data;
    switch (state) {
      case "loading":
        // console.log("ðŸ‘€ loading...");
        console.log(`ðŸ‘€ ${state}`);
        break;
      case "success":
        // console.log("ðŸ˜ƒ loaded");
        console.log(`ðŸ˜ƒ ${data.response.body}`);
        break;
      case "fail":
        // console.log("ðŸ˜± no network");
        console.log(`ðŸ˜± ${data.reason}`);
        break;
      default:
        throw new Error("Unknown Error");
    }
  }

  printLoginState({ state: "loading" }); // ðŸ‘€ loading...
  printLoginState({ state: "success", response: { body: "loaded" } }); // ðŸ˜ƒ loaded
  printLoginState({ state: "fail", reason: "no network" }); // ðŸ˜± no network
}
